export interface CustomTableProps {
  data: DataForTable[]; // Replace YourDataType with the appropriate type for your data
  togglePermission: (id: string, value: boolean) => void;
}

export interface DataForTable {
  id: string;
  email: string;
  permission: boolean;
  role: string;
}

export default function CustomTable(props: CustomTableProps) {
  const { data, togglePermission } = props;
  const [page, setPage] = useState(0);
  const [rowsPerPage, setRowsPerPage] = useState(5);
  function handleTogglePermission(id: string, value: boolean) {
    // Update the user's permissions in the state
    console.log(id);
    togglePermission(id, value);
  }

  // Avoid a layout jump when reaching the last page with empty rows.
  const emptyRows =
    page > 0 ? Math.max(0, (1 + page) * rowsPerPage - data.length) : 0;

  const handleChangePage = (
    event: React.MouseEvent<HTMLButtonElement> | null,
    newPage: number
  ) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (
    event: React.ChangeEvent<HTMLInputElement | HTMLTextAreaElement>
  ) => {
    setRowsPerPage(parseInt(event.target.value, 10));
    setPage(0);
  };

  return (
    <TableContainer sx={{ margin: 10 }} component={Paper}>
      <Table sx={{ minWidth: 500 }} aria-label="custom pagination table">
        <TableHead>
          <TableRow>
            <TableCell>
              {" "}
              <b>Email</b>
            </TableCell>
            <TableCell align="right">
              <b>Role</b>
            </TableCell>
            <TableCell align="right">
              <b>Permission</b>
            </TableCell>
          </TableRow>
        </TableHead>
        <TableBody>
          {(rowsPerPage > 0
            ? data.slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
            : data
          ).map((row) => (
            <TableRow key={row.email}>
              <TableCell component="th" scope="row">
                {row.email}
              </TableCell>
              <TableCell style={{ width: 160 }} align="right">
                {row.role}
              </TableCell>
              <TableCell style={{ width: 160 }} align="right">
                {row.role == "admin" ? (
                  <Switch defaultChecked disabled />
                ) : (
                  <Switch
                    checked={row.permission}
                    onChange={(e) => {
                      console.log("e.target.checked", e.target.checked);
                      handleTogglePermission(row.id, e.target.checked);
                    }}
                  />
                )}
              </TableCell>
            </TableRow>
          ))}
          {emptyRows > 0 && (
            <TableRow style={{ height: 53 * emptyRows }}>
              <TableCell colSpan={6} />
            </TableRow>
          )}
        </TableBody>
        <TableFooter>
          <TableRow>
            <TablePagination
              rowsPerPageOptions={[5, 10, 25, { label: "All", value: -1 }]}
              colSpan={3}
              count={data.length}
              rowsPerPage={rowsPerPage}
              page={page}
              SelectProps={{
                inputProps: {
                  "aria-label": "rows per page",
                },
                native: true,
              }}
              onPageChange={handleChangePage}
              onRowsPerPageChange={handleChangeRowsPerPage}
              ActionsComponent={TablePaginationActions}
            />
          </TableRow>
        </TableFooter>
      </Table>
    </TableContainer>
  );
}
